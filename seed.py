def upsert_test(course):
    questions = TEST_QUESTIONS.get(course.slug, [])
    for i, (text, options, correct_idx) in enumerate(questions, start=1):
        q = CourseQuestion.query.filter_by(course_id=course.id, text=text).first()
        if q is None:
            q = CourseQuestion(course_id=course.id)
            db.session.add(q)
        q.text = text
        q.position = i
        db.session.flush()
        for a in list(q.answers):
            db.session.delete(a)
        for j, opt in enumerate(options):
            db.session.add(CourseAnswer(question=q, text=opt, is_correct=(j == correct_idx)))


def upsert_course(data):
    course = Course.query.filter_by(slug=data["slug"]).first()
    if course is None:
        course = Course(slug=data["slug"])
        db.session.add(course)
    course.title = data["title"]
    course.description = data["description"]
    course.icon = data["icon"]
    course.level = data["level"]
    course.position = data["position"]
    db.session.flush()
    for i, (title, content) in enumerate(data["lessons"], start=1):
        lesson = Lesson.query.filter_by(course_id=course.id, title=title).first()
        if lesson is None:
            lesson = Lesson(course_id=course.id)
            db.session.add(lesson)
        lesson.title = title
        lesson.content = content
        lesson.position = i
    return course


def upsert_term(name, definition):
    t = Term.query.filter_by(term=name).first()
    if t is None:
        t = Term(term=name)
        db.session.add(t)
    t.definition = definition


def upsert_category(data):
    cat = Category.query.filter_by(slug=data["slug"]).first()
    if cat is None:
        cat = Category(slug=data["slug"])
        db.session.add(cat)
    cat.name = data["name"]
    cat.icon = data["icon"]
    cat.description = data["description"]
    return cat


def upsert_equipment(category, data):
    item = Equipment.query.filter_by(title=data["title"]).first()
    if item is None:
        item = Equipment()
        db.session.add(item)
    item.category = category
    for field in ("title", "marking", "purpose", "operating_principle",
                  "specifications", "application", "standards", "safety_notes"):
        setattr(item, field, data.get(field, ""))
    return item


def upsert_hotspot(item, label, x, y):
    hs = Hotspot.query.filter_by(equipment_id=item.id).first()
    if hs is None:
        hs = Hotspot(equipment=item)
        db.session.add(hs)
    hs.label, hs.x, hs.y = label, x, y


def ensure_admin():
    login = app.config["ADMIN_LOGIN"]
    if not User.query.filter_by(login=login).first():
        admin = User(login=login, full_name="Администратор", role="admin")
        admin.set_password(app.config["ADMIN_PASSWORD"])
        db.session.add(admin)
        print(f"  Создан администратор: {login} / {app.config['ADMIN_PASSWORD']}")


def run():
    with app.app_context():
        db.create_all()

        cats = {c["slug"]: upsert_category(c) for c in CATEGORIES}
        db.session.flush()

        for cat_slug, data in EQUIPMENT:
            item = upsert_equipment(cats[cat_slug], data)
            db.session.flush()
            if "hotspot" in data:
                label, x, y = data["hotspot"]
                upsert_hotspot(item, label, x, y)

        for name, definition in TERMS:
            upsert_term(name, definition)

        for course_data in COURSES:
            course = upsert_course(course_data)
            upsert_extra_lessons(course)
            upsert_test(course)

        ensure_admin()
        db.session.commit()
        print(f"Готово: категорий {Category.query.count()}, "
              f"изделий {Equipment.query.count()}, "
              f"точек на схеме {Hotspot.query.count()}, "
              f"терминов {Term.query.count()}, "
              f"курсов {Course.query.count()}, "
              f"уроков {Lesson.query.count()}, "
              f"вопросов {CourseQuestion.query.count()}.")


if __name__ == "__main__":
    run()
